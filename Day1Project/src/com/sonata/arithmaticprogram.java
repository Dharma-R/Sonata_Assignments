package com.sonata;

public class arithmaticprogram {

	public static void main(String[] args) {
		
	    int a = 14, b = 5;

	    System.out.println("a + b = " + (a + b));

	    System.out.println("a - b = " + (a - b));

	    System.out.println("a * b = " + (a * b));

	    System.out.println("a / b = " + (a / b));

	    System.out.println("a % b = " + (a % b));

	}

}
